// shorthand addition
var price = 120;
price += 30;
console.log("Addition is : ", price);

// shorthand subtraction
var price = 120;
price -= 20;
console.log("Subtraction is : ", price);

// multiplication
var price = 5;
price *= 6;
console.log("Multiplication is : ", price);

// division
var price = 30;
price /= 5;
console.log("Division is : ", price);
